// Función para obtener el icono basado en el tipo y proveedor
function icon($t = 'asterisk', $provider = 'b') {
    // Seleccionar el proveedor de iconos
    $icons = ($provider === 'f') ? get_fontawesome_icons() : get_bootstrap_icons();

    // Devolver el ícono usando la clase del proveedor correspondiente
    if (array_key_exists($t, $icons)) {
        return '<span class="' . $icons[$t] . '"></span>';
    } else {
        // Devuelve un ícono predeterminado si no se encuentra la clave
        return ($provider === 'f') ? '<span class="fa fa-folder"></span>' : '<span class="glyphicon glyphicon-folder-close"></span>';
    }
}

function icon_order_by($order_way = 'DESC') {
    $order_way = strtoupper($order_way);

    // Asegurarse de que el valor de order_way es seguro
    $allowed_order_way = ['ASC', 'DESC'];
    if (!in_array($order_way, $allowed_order_way)) {
        $order_way = 'DESC';
    }

    if ($order_way == 'DESC') {
        return icon("sort-by-attributes-alt");
    } else {
        return icon("sort-by-attributes");
    }
}

